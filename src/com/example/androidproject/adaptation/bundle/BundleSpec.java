package com.example.androidproject.adaptation.bundle;

public class BundleSpec {
	private int rawID;
	private String name;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRawID() {
		return rawID;
	}
	public void setRawID(int rawID) {
		this.rawID = rawID;
	};
}
